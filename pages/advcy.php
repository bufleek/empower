            <h2>Empowerment</h2>
            <h3>a)Positive Youth Development and Citizenship Practices</h3>
            <p> Development of a theoretical framework for youth participation in context of community development and
                policy formulation processes is a worthy cause to influence the young generations to embrace the future
                with positivity as interventions are timely and considerate to all.
                <br>
                There is a need to create an environment conducive to adolescents' taking responsibility for their
                quality-of-life issues by welcoming and enabling youth. Power has to be transferred to youth as
                responsibility for voicing, decision making, and action for the betterment of tomorrow. This will lead
                to
                positive changes in youth development and their social integration into the community.
            </p>
            <h4>Dialogue to deliberation</h4>
            <p>
                Deliberative dialogue and decision-making among youth is limited by their lack of knowledge and
                experience with social or policy issues worthy of deliberation, or that they underestimate potential
                negative consequences of either personal decisions or policy choices. To ensure youth can productively
                engage in institutional decision-making, a variety of efforts have to be made to enhance their
                understanding of policy issues, develop the skills needed to engage in effective dialogue, and create
                opportunities for youth engagement in decision making processes.
            </p>
            <h4>Coping with Youth Violence</h4>
            <p>
                Uninformed youthful behaviors can represent a great problem to the society if not well managed, their
                engagement in peace
                forums to create awareness on importance of peace stability to their lives even in the remotest areas in
                the country is a chance to restoration of stability.
                Parents as well can be taught on how to equip their children with necessary values which will help
                youths grow responsibly.
            </p>
            <h4>Adolescent and Youth Sexual and Reproductive Health</h4>
            <p>
                Youths at their growing eves their sexual health is very sensitive and should apply compelling was
                to mitigate possible threats. There are nearly two billion young people in the world today who have
                specific health and development needs, and many face challenges that threaten their well-being,
                including poverty, a lack of access to health information and services, and unsafe environments.
                <br>
                Investing in adolescents can accelerate the fight against poverty, inequality and gender
                discrimination.
                <br>
                Preconception care and contraceptive use can prevent unintended pregnancies, unsafe abortions and
                sexually-transmitted infections in adolescent girls and young women (Dean, Lassi, Imam and Bhutta,
                2014).
                <br>
                Although adolescents are commonly referred to as the "next" or "future generation", they require
                protection and care, services, opportunities, support, and recognition now (UNICEF, 2011).
            </p>
            <h3>b)Health education advocacy through cultural awareness.</h3>
            <p>
                Culture is a pattern of ideas, customs and behaviors shared by a society which defines their lifestyle
                in
                posterity to generations and connects the past, present and the future. Health is a concept of culture
                which is articulated in every society, in the normative way of living people had compelling ways to make
                sure their members are healthy. The current globalization changes in social evolution where dietary
                patterns completely changed and environment also changed, health deliberations have advanced to be more
                complex, outdated interventions based on culture used centuries ago won’t help any more.<br>
                Pastoral communities living in the peripherals of Kenya, marginalized in resource distributions and with
                very less involvement in their lives development, they only see interventions rolled out which barely
                meets their specific needs.<br>
                Hygiene ranging from individual consumption to resources like water, sanitation speaks a lot to these
                communities. This doesn’t matter who rolls the interventions, the interventions should be measured,
                evaluated and predetermined to influence these communities in all dimensions positively.<br>
                The increase rates of teen pregnancies in these communities is mostly due to lack of education on lucid
                approaches to protect themselves. Advocating for health interventions to be intensified will be an
                absolute baseline to address community health authentically through provision of education on proper
                approaches to health provisions.
            </p>
